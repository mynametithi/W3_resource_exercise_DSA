// Write a program in C++ to divide two numbers and print them on the screen:
#include <iostream>

using namespace std;

int main()
{
    cout << "\n\n Divide two numbers and print:\n";
    cout << "----------------------------------\n";

    int a;
    int b;
    int resdiv;

    a = 30;
    b = 10;
    resdiv = a / b;

    cout << " The quotient of " << a << " and " << b << " is : " << resdiv << "\n\n";

    return 0;
}
